// ── LogoutTx ──────────────────────────────────────────────────────────────────

// LogoutTx atomically:
//  1. Revokes the token identified by JTI (reason = 'logout').
//  2. Ends the session identified by SessionID.
//  3. Writes an audit log row event_type = 'logout'.
//
// The fn_revoke_token_family trigger intentionally skips reason='logout', so
// only this device's token is invalidated — sibling tokens in the same family
// remain valid. Each device manages its own session independently.
func (s *Store) LogoutTx(ctx context.Context, in LogoutTxInput) error {
	h, err := s.BeginOrBind(ctx)
	// Unreachable: BeginOrBind with TxBound=true never calls Pool.Begin
	// and always returns nil error. No test can trigger this branch.
	if err != nil {
		return fmt.Errorf("store.LogoutTx: begin tx: %w", err)
	}

	jtiPg := s.ToPgtypeUUID(in.JTI)
	sessionPg := s.ToPgtypeUUID(in.SessionID)
	userPg := s.ToPgtypeUUID(in.UserID)

	// 1. Revoke this token only (single row; AND revoked_at IS NULL is idempotent).
	if _, err := h.Q.RevokeRefreshTokenByJTI(ctx, db.RevokeRefreshTokenByJTIParams{
		Reason: "logout",
		Jti:    jtiPg,
	}); err != nil {
		h.Rollback()
		return fmt.Errorf("store.LogoutTx: revoke token: %w", err)
	}

	// 2. End the device session (AND ended_at IS NULL is idempotent).
	if err := h.Q.EndUserSession(ctx, sessionPg); err != nil {
		h.Rollback()
		return fmt.Errorf("store.LogoutTx: end session: %w", err)
	}

	// 3. Audit log.
	if err := h.Q.InsertAuditLog(ctx, db.InsertAuditLogParams{
		UserID:    userPg,
		EventType: string(audit.EventLogout),
		Provider:  db.AuthProviderEmail,
		IpAddress: s.IPToNullable(in.IPAddress),
		UserAgent: s.ToText(s.TruncateUserAgent(in.UserAgent)),
		Metadata:  []byte("{}"),
	}); err != nil {
		h.Rollback()
		return fmt.Errorf("store.LogoutTx: audit log: %w", err)
	}

	// Unreachable via QuerierProxy: on the TxBound path commitFn is a no-op
	// that always returns nil; on the non-TxBound path commitFn wraps
	// pgx.Tx.Commit which the proxy cannot intercept.
	if err := h.Commit(); err != nil {
		return fmt.Errorf("store.LogoutTx: commit: %w", err)
	}
	return nil
}
