// compile-time check that *Store satisfies Storer.
var _ Storer = (*Store)(nil)

// Store provides data access for the session feature.
// It embeds authshared.BaseStore for pool management, transaction handling,
// and type-conversion helpers.
type Store struct {
	authshared.BaseStore
}

// NewStore constructs a Store backed by pool.
func NewStore(pool *pgxpool.Pool) *Store {
	return &Store{BaseStore: authshared.NewBaseStore(pool)}
}

// WithQuerier returns a shallow copy of s bound to q with TxBound=true.
// Used in tests to scope all writes inside a rolled-back transaction.
func (s *Store) WithQuerier(q db.Querier) *Store {
	c := *s
	c.BaseStore = s.BaseStore.WithQuerier(q)
	return &c
}

// ── GetRefreshTokenByJTI ──────────────────────────────────────────────────────

// GetRefreshTokenByJTI fetches a refresh_tokens row by its jti.
// Returns authshared.ErrTokenNotFound when the jti is absent.
func (s *Store) GetRefreshTokenByJTI(ctx context.Context, jti [16]byte) (StoredRefreshToken, error) {
	row, err := s.Queries.GetRefreshTokenByJTI(ctx, s.ToPgtypeUUID(jti))
	if err != nil {
		if s.IsNoRows(err) {
			return StoredRefreshToken{}, authshared.ErrTokenNotFound
		}
		return StoredRefreshToken{}, fmt.Errorf("store.GetRefreshTokenByJTI: query: %w", err)
	}
	return StoredRefreshToken{
		JTI:       row.Jti.Bytes,
		UserID:    row.UserID.Bytes,
		SessionID: row.SessionID.Bytes,
		FamilyID:  row.FamilyID.Bytes,
		ExpiresAt: row.ExpiresAt.Time.UTC(),
		IsRevoked: row.RevokedAt.Valid,
	}, nil
}

// ── GetUserVerifiedAndLocked ─────────────────────────────────────────────────

// GetUserVerifiedAndLocked fetches email_verified, is_locked, and is_active
// for the given user. Returns authshared.ErrUserNotFound on no-rows.
